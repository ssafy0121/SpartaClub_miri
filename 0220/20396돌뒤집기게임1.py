# 첫 줄: 테스트 케이스
# 예: 3 을 입력하면 3번의 테스트 케이스를 돌린다는 뜻
T = int(input())

# range(1, 1+T) → 1부터 T까지 숫자를 하나씩 뽑아줌
# tc에는 1, 2, 3, ... 이런 식으로 번호가 들어감
for tc in range(1, 1+T):

    # N: 배열의 길이 (몇 개 숫자가 있는지)
    # M: 업데이트(덮어쓰기) 명령이 몇 번 들어오는지
    # 예) 5 3 → 길이 5인 배열, 3번 업데이트할 예정
    N, M = map(int, input().split())

    # 숫자 N개를 한 줄에 입력받아서 리스트로 만듦
    # 예) 3 7 4 2 9 → arr = [3, 7, 4, 2, 9]
    arr = list(map(int, input().split()))

    # 이번 테스트케이스에서 총 M번의 업데이트를 할 거임
    for _ in range(M):           # _ 는 "이 변수는 안 쓸 거야"라는 의미로 자주 씀

        # i, j 를 입력받음
        # i: 시작 위치 (1부터 시작하는 번호!)
        # j: 몇 칸을 덮어쓸 건지 (길이)
        i, j = map(int, input().split())

        # 실제 파이썬 리스트 인덱스는 0부터 시작하니까
        # i-1 번째 위치부터 시작해야 함

        # end = min(N, i-1 + j)
        # → i-1부터 j칸 만큼 가려고 했는데,
        #   배열 끝(N)을 넘어가면 안 되니까 min()으로 막아줌
        # 예) N=5, i=4, j=3 → 4-1=3부터 3+3=6까지 가려는데
        #     배열은 0~4까지만 있으니까 end=5로 제한됨
        end = min(N, i-1 + j)

        # 가장 중요한 부분!
        # i-1 위치에 있는 값을 기준으로
        # i-1 부터 end-1 까지 전부 그 값으로 바꿔버림
        for k in range(i-1, end):
            # arr[k] ← arr[i-1] 에 있는 값을 복사해서 덮어쓰기
            arr[k] = arr[i-1]

    # 한 테스트케이스 끝났을 때
    # 출력 형식: #1 3 3 3 2 9
    #           #2  ...
    # print(f'#{tc}', *arr) → *arr 는 리스트를 풀어서 띄어쓰기로 출력해줌
    print(f'#{tc}', *arr)